/* 
 * bitXor - x^y using only ~ and & 
 *   Example: bitXor(4, 5) = 1
 *   Legal ops: ~ &
 *   Max ops: 14
 *   Rating: 1
 */
int bitXor(int x, int y) {
  return ~(~((~(x & y) & ~(~x & ~y))));
}
/*
 * absVal - absolute value of x
 *   Example: absVal(-1) = 1.
 *   You may assume -TMax <= x <= TMax
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 10
 *   Rating: 4
 */
int absVal(int x) {
  // x >> 31 will be all 1 mask if x is negative and all 0 mask otherwise
  int mask = x >> 31;
  return (mask & (~x + 1)) | (~mask & x);
}
/*
 * isTmax - returns 1 if x is the maximum, two's complement number,
 *     and 0 otherwise
 *   Legal ops: ! ~ & ^ | +
 *   Max ops: 10
 *   Rating: 1
 */
int isTmax(int x) {
  return !(~(x + x + 1) | !(x + 1));
}
/*
 * isLessOrEqual - if x <= y  then return 1, else return 0 
 *   Example: isLessOrEqual(4,5) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 24
 *   Rating: 3
 */
int isLessOrEqual(int x, int y) {
  // x32 has 32 bits that share the value of x's sign bit. Similar is y32.
  int x32 = x >> 31;
  int y32 = y >> 31;
  // cond_dif_sign is all 1 when x and y have different sign bit.
  int cond_dif_sign = x32 ^ y32;
  int TMax = ~(1<<31);
  // xp sets the negative bit of x to 0. Similar is yp.
  int xp = TMax & x;
  int yp = TMax & y;
  int xpMinyp = xp + (~yp + 1);
  int ret =
  (cond_dif_sign & // when x and y have different signs
  !!x32) // return 1 if x is negative, 0 elsewise
  |
  (~cond_dif_sign & (//when x and y have the same signs
  (!!(xpMinyp >> 31) | !xpMinyp) //xpMinyp <= 0
  ));
  return ret;
}
/*
 * byteSwap - swaps the nth byte and the mth byte
 *  Examples: byteSwap(0x12345678, 1, 3) = 0x56341278
 *            byteSwap(0xDEADBEEF, 0, 2) = 0xDEEFBEAD
 *  You may assume that 0 <= n <= 3, 0 <= m <= 3
 *  Legal ops: ! ~ & ^ | + << >>
 *  Max ops: 25
 *  Rating: 2
 */
int byteSwap(int x, int n, int m) {
  const int mask = 0xFF;
  const int nbites = n << 3;
  const int mbites = m << 3;
  // Extract the desired bytes
  const int nmask = mask << nbites;
  const int mmask = mask << mbites;
  const int nbyte = nmask & x;
  const int mbyte = mmask & x;
  // shift the extracted bites into desired positions
  // we need to apply mask to them to avoid extra 1s generated by
  // potential arithmetic shifts
  const int new_mbyte = ((nbyte >> nbites) << mbites) & mmask;
  const int new_nbyte = ((mbyte >> mbites) << nbites) & nmask;
  // Turn the bites occupied by the original nbyte to 0
  int ret = x;
  ret = ret & (~nmask);
  // Add the new nbyte, the original mbyte, back into x
  ret = ret + new_nbyte;
  // Turn the bites occupied by the original mbyte to 0
  ret = ret & (~mmask);
  // Add the new mbyte, the original nbyte, back into x
  ret = ret + new_mbyte;
  return ret;
}
/*
 * rotateLeft - Rotate x to the left by n
 *   Can assume that 0 <= n <= 31
 *   Examples: rotateLeft(0x87654321,4) = 0x76543218
 *   Legal ops: ~ & ^ | + << >> !
 *   Max ops: 25
 *   Rating: 3
 */
int rotateLeft(int x, int n) {
  // First get only the bits that are wrapped around
  // Flip 0 everything else
  int wrap = (x >> (32 + (~n + 1))) & ~((~0) <<n);
  // Then get the bits that are not wrapped
  int shift = x << n;
  // Combine them to get the result
  return wrap + shift;
}
/*
 * isPower2 - returns 1 if x is a power of 2, and 0 otherwise
 *   Examples: isPower2(5) = 0, isPower2(8) = 1, isPower2(0) = 0
 *   Note that no negative number is a power of 2.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 4
 */
int isPower2(int x) {
  return !(!x + ((x + ((x >> 31) ^ (~0))) & x));
}
/* 
 * allEvenBits - return 1 if all even-numbered bits in word set to 1
 *   Examples allEvenBits(0xFFFFFFFE) = 0, allEvenBits(0x55555555) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 2
 */
int allEvenBits(int x) {
  // Remark that 0b1010 equals 0xAA.
  int oddBits = (0xAA << 8) + 0xAA;
  oddBits = (oddBits << 16) | oddBits;
  // oddBits | x = -1 iff all even bits in x are set to 1
  return !(~(oddBits | x));
}
